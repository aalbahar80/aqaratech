<script lang="ts">
	import Hoverable from '$lib/components/Hoverable.svelte';
	import { classes } from '$lib/utils/classes';
	import { startCase } from '$lib/utils/common';
	import { ListboxOption } from '@rgossiaux/svelte-headlessui';
	import type { HitDto } from '@self/sdk';
	import * as R from 'remeda';
	import type { SvelteComponentTyped } from 'svelte';

	export let item: HitDto;
	export let icon: typeof SvelteComponentTyped<
		svelte.JSX.IntrinsicElements['svg']
	>;

	const hiddenFields = ['title', 'id', 'organizationId'];

	$: highlightedFields =
		R.pickBy(
			item.formatted,
			(val, key: string) =>
				typeof val === 'string' &&
				val.includes('<mark>') &&
				!hiddenFields.includes(key) &&
				key in item &&
				// @ts-expect-error
				item[key] !== item.title && // hide any field if it matches title
				(key !== 'label' || !item.formatted?.title?.includes('<mark>')), // hide label if title is already highlighted
		) ?? {};
</script>

<Hoverable let:hovering>
	<ListboxOption value={item}>
		<div
			class={classes(
				'cursor-default select-none px-4 py-2 [&_mark]:underline [&_mark]:underline-offset-2',
				hovering
					? 'bg-indigo-600 text-white [&_mark]:text-white [&_mark]:bg-inherit'
					: '[&_mark]:text-indigo-600 [&_mark]:bg-inherit [&_mark]:decoration-indigo-500 [&_mark]:decoration-2',
			)}
		>
			<div class="flex">
				<svelte:component
					this={icon}
					class={classes(
						'mr-2 h-6 w-6 flex-none text-opacity-40',
						hovering ? 'text-white text-opacity-70' : 'text-gray-400',
					)}
					aria-hidden="true"
				/>
				<div class="flex flex-col gap-y-1">
					<p>
						{@html item.formatted?.title}
					</p>
					<div class="flex flex-col gap-y-1 text-xs font-light">
						{#each Object.entries(highlightedFields) as [key, val]}
							<p>
								<span class="font-extralight">
									{startCase(key)}:
								</span>
								{@html val}
							</p>
						{/each}
					</div>
				</div>
			</div>
		</div>
	</ListboxOption>
</Hoverable>
